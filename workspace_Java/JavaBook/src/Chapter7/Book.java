package Chapter7;

class Bookbase{
	
	int price;
	String title, author;
	String test;
	public char[] toString;
	
	Bookbase(int price,String title,String author){
		this.price = price;
		this.title = title;
		this.author = author;
	
		this.test = "String 출력 : "+price+" "+" "+title+" "+" "+author;
		
	}
}

public class Book{
	
	public static void main(String[] args) {
		
		Bookbase[] Books = new Bookbase[3];
			Books[0] = new Bookbase(20000,"java7","인경렬");
			Books[1] = new Bookbase(2500,"열혈강호","양재현");
			Books[2] = new Bookbase(12000,"무소유","장혜민");

		for(Bookbase sysbook : Books) {
			System.out.println(sysbook.price+" "+" "+sysbook.title+" "+" "+sysbook.author);
		}
		
		System.out.println();
		
		for(Bookbase sys : Books) {
			System.out.println(sys.test);
		}
		
		System.out.println();
		
		for(Bookbase sysbook : Books) {
			System.out.println(sysbook);
		}
	}
}

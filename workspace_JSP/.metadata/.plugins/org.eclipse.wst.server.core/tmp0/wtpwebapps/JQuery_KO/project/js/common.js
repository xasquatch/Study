	  var scv = $(window).scrollTop();
	  //스크롤바가 이동될때마다 <div id="quick_menu">태그에 애니메이션 메서드가 적용됨.
	  //이때 scv변수값(스크롤바의 이동 높이값)을 top속성에 적용하여
	  //1초만에 스크롤바의 이동거리만큼 <div id="quick_menu">태그가 이동되도록 함
	  //그리고 index.html을 처음실행했을때 문서 상단에서 퀵메뉴의 여백 거리값 defaultTop변수의 ㄱ밧을 
	  //더하여 상단에서 여백거리만큼떨어져서 이동되도록함.

  //index.html 처음실행했을때..문서 상단에서 퀵 메뉴의 여백 거릿값을 구하기 위해
  //position의 속성의 top값을 구함
  //이때  px단위가 붙게 되는데 정수만 남기기 위해 parseInt()메소드를 사용하여 정수값만 얻는다
  //요약 : 기본 문서 상단에서 퀵메뉴가 이동한 거릿값을 구한다
  var defaultTop = parseInt($("#quick_menu").css("top"));
  
 
  
